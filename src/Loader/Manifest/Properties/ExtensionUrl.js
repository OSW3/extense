/**
 * Homepage_url Property
 * --
 * Generate the "homepage_url" property of manifest.json
 * 
 * @version 1.0
 * @since 1.0
 */

'use strict';


/******************************************************************************/
/******************************************************************************/
/******************************************************************************/
// Imports

const ManifestConfig = require('../ManifestConfig');


/******************************************************************************/
/******************************************************************************/
/******************************************************************************/
// Consts

const PROPERTY_ID = 'homepage_url';


/******************************************************************************/
/******************************************************************************/
/******************************************************************************/
// Exports

module.exports = class ExtensionUrl extends ManifestConfig
{
    getProperty()
    {
        let value = this.config.extension.url;
        
        if (value)
        {
            return {[`${PROPERTY_ID}`]: value};
        }
    }
}